\ Интерактивные

: INTERPRET
  l6004 @ ?DUP IF EXECUTE EXIT THEN
  REPEAT
    BL WORD FIND
    DUP IF
      0< STATE @ AND IF , ELSE EXECUTE THEN
    ELSE
      DROP NUMBER DPL @ 1+ IF DLITERAL ELSE DROP LITERAL THEN
    THEN
    ?STACK
  AGAIN
;

: FIND
  l6006 @ ?DUP IF EXECUTE EXIT THEN
  CONTEXT @ ?WORD
  IF
    TRUE
  ELSE
    CURRENT @ DUP CONTEXT @ =
    IFNOT
      ?WORD
    ELSE
       DROP FALSE
    THEN
  THEN
  IF
    DUP NAME> SWAP C@ 80 AND
    IF 1 ELSE -1 THEN
  ELSE
    FALSE
  THEN
;

: LITERAL
  STATE @ IF COMPILE LIT , THEN
; IMMEDIATE

: DLITERAL
  STATE @ IF COMPILE DLIT , , THEN
; IMMEDIATE

: IMMEDIATE
  LATEST 80 TOGGLE
;

: QUIT
  l6000 @ ?DUP IF EXECUTE THEN
  R0 @ RP!
  STANDIO
  CR
  [
  FORTH DEFINITIONS
  BEGIN
    INTERPRET
  AGAIN
;

: PROMPT ( -- )
  \ Режим трансляции
  STATE @ IF C" C ELSE C" I THEN EMIT
  \ Система счисления
  BASE @ DUP DECIMAL 2 .R BASE !
  \ Галочка и пробел
  C" > EMIT SPACE
;

: QUERY
  CR
  PROMPT \ C" > EMIT
  TIB 4F EXPECT CR TIB SPAN @ 0 TRUE
;
