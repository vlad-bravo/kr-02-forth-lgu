: INTERPRET
  l6004 @ ?DUP IF EXECUTE EXIT THEN
  REPEAT
    BL WORD FIND
    DUP IF
      0< STATE @ AND IF , ELSE EXECUTE THEN
    ELSE
      DROP NUMBER DPL @ 1+ IF DLITERAL ELSE DROP LITERAL THEN
    THEN
    ?STACK
  AGAIN
;

: FIND
