    TRUE
  ELSE
    CURRENT @ DUP CONTEXT @ =
    IFNOT
      ?WORD
    ELSE
       DROP FALSE
    THEN
  THEN
  IF
    DUP NAME> SWAP C@ 80 AND
    IF 1 ELSE -1 THEN
  ELSE
    FALSE
  THEN
;

